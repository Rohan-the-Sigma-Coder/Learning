def fizz_buzz2():
    num = 1
    for i in range(100):
        if num % 3 == 0 and num % 5 == 0:
            print('FizzBuzz')
        elif num % 3 == 0:
            print('Fizz')
        elif num % 5 == 0:
            print('Buzz')
        else:
            print(num)
        num += 1


fizz_buzz2()